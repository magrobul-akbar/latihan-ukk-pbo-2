    private void cekLogin() {
        try {
            String query = "SELECT * FROM pengguna WHERE nama_pengguna = '" + namaPengguna.getText() + "' AND password = '" + password.getText() + "'";
            Connection con = Koneksi.koneksiDB();
            Statement stmt = con.createStatement();
            ResultSet result = stmt.executeQuery(query);

            if (result.next()) {
                Boolean cekNamaPengguna = namaPengguna.getText().equals(result.getString("nama_pengguna"));
                Boolean cekPassword = password.getText().equals(result.getString("password"));
                
                if (cekNamaPengguna && cekPassword) {
                    new Dashboard().show();
                    this.dispose();
                }

            } else {
                JOptionPane.showMessageDialog(null, "Login gagal periksa nama pengguna atau email");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

